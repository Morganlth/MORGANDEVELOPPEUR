<div
class="features"
style:transform="translateY({features_TRANSLATE_Y}%)"
>
    {#each prop_FEATURES as group}
        <div
        class="container"
        class:show={group.show}
        >
            {#each group.contents as feature, j}
                <div
                style:--feature-direction={j % 2 ? -1 : 1}
                style:--feature-delay="{.2 * j}s"
                >
                    <Line>
                        <span
                        slot="id"
                        >
                            {j}
                        </span>

                        <section
                        slot="content"
                        >
                            <h3
                            data-topic={feature.topic}
                            >
                                {feature.topic}
                            </h3>

                            <svelte:element
                            this={feature.html ?? 'p'}
                            class="feature"
                            {...feature.props}
                            data-content={feature.data}
                            tabindex={feature.html === 'a' && prop_FOCUS ? 0 : -1}
                            >
                                {feature.data}
                            </svelte:element>
                        </section>
                    </Line>

                    {#if feature.child}
                        <svelte:component
                        this={feature.child}
                        />
                    {/if}
                </div>
            {/each}
        </div>
    {/each}
</div>


<!-- #|-SCRIPT-| -->

<script>

// #\-IMPORTS-\

    // --DATA

    // --SVELTE

    // --LIB
    import { wait_throttle } from '$lib/wait'

    // --CONTEXTS

//=======@COMPONENTS|

    // --*
    import Line from '../../../../global/covers/Line.svelte'
    
//=======@STYLE|

    // --*
    import '../../../../assets/scss/global/features.scss'


// #\-EXPORTS-\

    // --PROPS
    export let
    prop_FOCUS = false
    ,
    prop_RATIO    = 0,
    prop_FRACTION = 0
    ,
    prop_FEATURES

    // --BINDING


// #\-CONSTANTES-\

    // --SVELTE

    // --CONTEXTS

    // --OUTSIDE

    // --THIS

    // --INSIDE


// #\-VARIABLES-\

    // --CONTEXTS

    // --OUTSIDE

    // --THIS
    let features_TRANSLATE_Y = 0

    // --INSIDE


// #\-REATIVES-\

    // --CONTEXTS

    // --OUTSIDE

    // --THIS
    $: wait_throttle(features_update, 3, 4)(prop_RATIO) // +- 50ms, +- 70ms

    // --INSIDE


// #\-FUNCTIONS-\

//=======@LIFE|

    // --SVELTE

    // --SET

    // --GET

    // --UPDATES
    function features_update(ratio)
    {
        features_TRANSLATE_Y = -100 * ratio

        for (let i = prop_FEATURES.length - 1; i >= 0; i--)
        {
            const
            FEATURE = prop_FEATURES[i],
            SHOW    = ratio >= FEATURE.id * prop_FRACTION
        
            prop_FEATURES[i] = { ...FEATURE, show: SHOW }
        }
    }

    // --DESTROY


//=======@COMMANDS|

    // --*


//=======@EVENTS|

    // --*


//=======@TRANSITIONS|

    // --*


//=======@ANIMATIONS|

    // --*


//=======@UTILS|

    // --*


</script>


